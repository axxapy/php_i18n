	public static function getCountryByMobileOperator($mcc) {
		foreach (self::$countries_mcc_codes as $cc => $mList) {
			if (is_array($mcc) && in_array($mcc, $mList)) {
				return $cc;
			} elseif ($mList == $mcc) {
				return $cc;
			}
		}

