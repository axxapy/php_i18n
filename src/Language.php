	private static $rtl_languages = [
		self::AR,
		self::IW,
		self::HE,
		self::UR,
		self::FA,
	];

	/**
	 * Search for language by code in different formats: 2 letter or 3 letter codes are valid
	 *
	 * @param string $code
	 *
	 * @return array|null
	 */
	public static function findLanguageByCode($code) {
		foreach (self::$languages as $lang) {
			if ($lang['code'] == $code || $lang['code_b'] == $code || $lang['code_t'] == $code) return $lang;
		}
		return null;
	}

	/**
	 * Returns language by 2-letter code
	 *
	 * @param string $code
	 *
	 * @return array|null
	 */
	public static function getLanguageByCode($code) {
		return isset(self::$languages[$code]) ? self::$languages[$code] : null;
	}

	/**
	 * Returns language direction by code
	 *
	 * @param string $code
	 *
	 * @return string self::DIR_*
	 */
	public static function getDirectionByCode($code) {
		return in_array($code, self::$rtl_languages) ? self::DIR_RTL : self::DIR_LTR;
	}

	public static function isValidLanguage($code){
		return self::getLanguageByCode($code) !== null;
	}
